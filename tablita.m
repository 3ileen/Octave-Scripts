function tablita(f,t0,y0,T,p)
  [t,y1]=EULER(f,t0,y0,T,p);
  [t,y2]=heun(f,t0,y0,T,p);
  [t,y3]=rk3(f,t0,y0,T,p);
  [t,y4]=rk4(f,t0,y0,T,p);
  y=t.*sin(t)+t.^2;
  fprintf('             -----------------------------------------------------\n');
  fprintf('             |                        y(n)                       |\n');
  fprintf('-------------------------------------------------------------------------------\n');
  fprintf('|     t(n)   |    EULER   |    HEUN    |     RK3    |     RK4    |   y(t(n))  |\n');
  fprintf('-------------------------------------------------------------------------------\n');
  for k=1:p+1
    fprintf('| %10.6f | %10.6f | %10.6f | %10.6f | %10.6f | %10.6f |\n',t(k),y1(k),y2(k),y3(k),y4(k),y(k));
  endfor
  fprintf('-------------------------------------------------------------------------------\n');
  hold on
  plot(t,y1,'*b')
  plot(t,y2,'*r')
  plot(t,y3,'*g')
  plot(t,y4,'*y')
endfunction
